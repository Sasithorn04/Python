#โปรแกรมรายการอาหารร้านศศิธร
def displayFood(item):
    for i in range(len(item)):
        print("รายการอาหารชนิดที่",i+1,"=",item[i])
food = ["ข้าวขาหมู","ข้าวมันไก่","ก๋วยเตี๋ยวน้ำตก","ราดหน้า"]
displayFood(food)

